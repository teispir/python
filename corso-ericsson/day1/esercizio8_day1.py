'''
somma
sottrazione
moltiplicazione
divisione
divisione (quoziente intero)
modulo (resto)
elevamento a potenza.
'''
a = 5
b = 3
# somma
print("somma:", a + b)
# sottrazione
print("sottrazione:", a - b)
# moltiplicazione
print("moltiplicazione:", a * b)
# divisione
print("divisione: ", a / b)
# divisione (quoziente intero)
print("divisione a quoziente intero:", a // b)
# modulo (resto)
print("resto della divisione:", a % b)
# elevamento a potenza
print("potenza:", a**b)