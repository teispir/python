# Definizione delle variabili (numeri)
risultatoSomma = 12.0 + 3
risultatoSottrazione = 5 - 2
risultatoProdotto = 5 * 3
risultatoDivisione = 9 / 3
risultatoDivisioneIntera = 9.0 // 2
risultatoDivisioneResto = 9 % 2
risultatoPotenza = 3 ** 2

# Stampa delle variabili (numeri)
print("--------------------------")
print("Variabili (numeri)")
print("--------------------------")

print("Somma:", risultatoSomma)
print("Sottrazione:", risultatoSottrazione)
print("Prodotto:", risultatoProdotto)
print("Divisione:", risultatoDivisione)
print("Divisione Intera:", risultatoDivisioneIntera)
print("Resto Divisione:", risultatoDivisioneResto)
print("Somma:", risultatoPotenza)

